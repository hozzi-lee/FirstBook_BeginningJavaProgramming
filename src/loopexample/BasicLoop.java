package loopexample;

public class BasicLoop {
	public static void main(String[] args) {
		
		int num = 1;
		num += 2; // --> num = num + 2 와 같다.
		num += 3; // --> num = num + 3 와 같다.
		num += 4; // --> num = num + 4 와 같다.
		num += 5; // --> num = num + 5 와 같다.
		num += 6; // --> num = num + 6 와 같다.
		num += 7; // --> num = num + 7 와 같다.
		num += 8; // --> num = num + 8 와 같다.
		num += 9; // --> num = num + 9 와 같다.
		num += 10; // --> num = num + 10 와 같다.
		
		System.out.println("1부터 10까지의 합은 " + num + "입니다.");
	}

}
