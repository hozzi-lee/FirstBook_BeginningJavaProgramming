package chapter2;

public class CharacterEx2 {
	public static void main(String[] args) {

		char ch1 = '한'; // 문자형 한 그대로 출력
		char ch2 = '\uD55C'; // 문자형 한 유니코드값
		char ch3 = 54620; // 문자형 한 아스키값

		System.out.println(ch1); // 한
		System.out.println(ch2); // 한
		System.out.println(ch3); // 한
		
		// 한글 유니코드 www.unicode.org/charts/PDF/UAC00.pdf

	}

}
