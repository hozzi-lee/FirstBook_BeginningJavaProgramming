package chapter2;

public class Variable1 {
	public static void main(String[] args) {
		int level; // 정수형 변수(integer) level을 선언
		level = 10; // level 변수에 값 10을 대입
		System.out.println(level); // level 값 출력
		
		// 1분 복습
		int age; // 정수형 변수(integer) age를 선언
		age = 29; // age 변수에 값 29를 대입
		System.out.println(age); // age 값 출력
	}

}
